public class EvenOrOdd {
    public static void main(String args[])
    {
        Number(10);
    }

    public static void Number(int i)
    {
        if(i%2==0)
        {
            System.out.println("The given number is Even");
        }
        else
        {
            System.out.println("The given number is oDD");
        }
    }
}
